"use strict";
// var - reassignment and declaration is possible
var is_name = "Virat";
console.log(is_name);
var is_name = "kohli";
console.log(is_name);
is_name = "Virat Kohli";
console.log(is_name);
// let - reassignment is only possible
let his_name = "Rahul Dravid";
console.log(his_name);
his_name = "Rahul";
console.log(his_name);
his_name = "Dravid";
console.log(his_name);
// const - no reassgnment and declaration
const cric_name = "Rohit";
console.log(cric_name);
